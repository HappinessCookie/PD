                <div class="division-info clearfix">
                    <h2 class="page-header">КР (конструктивные решения)</h2>
                    <p>КР (конструктивные решения) раздел в составе проектного документа, обоснованный расчетами,
                       строительными нормами и государственными стандартами проектного решения раздела АР.</p>
                    <p>Основой данного раздела является определение объема строительных работ, подбор оптимальных
                       строительных конструкций под определённые виды работ, с целью безопасности эксплуатации здания
                       или сооружения.</p>
                    <p>Над данным разделом работают конструкторы, которые описывают и обосновывают технические решения
                       здания, с учетом розы ветров, действующих нагрузок на здание, - от здания на фундамент, монтажные
                       схемы и пояснительные записки, а так же рассчитываются узловые соединения, конструктивные
                       элементы и детали соединения всего здания.</p>
                    <p>Данный раздел является неотъемлемой частью проектной документации АС, которая является
                       основополагающей для будущего здания, с учетом функциональности, экономичности и технических
                       характеристик.</p>
                    <p>Конструктивный раздел выдается одним из решений, в зависимости от материала здания или
                       сооружения: КЖ (конструкции железобетонные), КМ (конструкции металлические) и КД (конструкции
