            <p>КР (конструктивные решения) раздел в составе проектного документа, обоснованный расчетами,
               строительными нормами и государственными стандартами проектного решения раздела АР.</p>
            <p>Основой данного раздела является определение объема строительных работ, подбор оптимальных
